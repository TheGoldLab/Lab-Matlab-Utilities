function anames_ = getPLX_analogNames(channels)
% function anames_ = getPLX_analogNames(channels)
%

% Copyright 2005 by Joshua I. Gold
%   University of Pennsylvania
%
% history:

% channels 1-12 are presumed to be for LFP 
%   (local field potential) measurements
% remaining chanels are pupil diameter and horizontal
%   and vertical eye position
anames_ = { ...
    'LFP01' ...
    'LFP02' ...
    'LFP03' ...
    'LFP04' ...
    'LFP05' ...
    'LFP06' ...
    'LFP07' ...
    'LFP08' ...
    'LFP09' ...
    'LFP10' ...
    'LFP11'          ...
    'unknown1'       ...
    'unknown2'       ...
    'pupil_diameter' ...
    'horiz_eye'      ...
    'vert_eye'};

if nargin == 1 && ~isempty(channels)
    
    % always assume that channels >= 12 are ordered as above
    Lch = channels>=12;
    nch = sum(Lch);
    if nch >= 1 && nch <= 4
        channels(Lch) = (1:nch)+length(anames_)-nch;
    end
    if any(channels<1) || any(channels>length(anames_))
        error('bad analog channels');
    end
    anames_ = anames_(channels);
end
    